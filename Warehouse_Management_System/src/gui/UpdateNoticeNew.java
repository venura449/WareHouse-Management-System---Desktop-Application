    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String title = jTextField1.getText();
        String notice = jTextArea1.getText();

        if (title.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter title.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (notice.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter notice.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                MySQL.execute("UPDATE `notice` SET `title`='" + jTextField1.getText() + "',`notice`='" + jTextArea1.getText() + "' "
                        + "WHERE `idnotice`='" + jTextField2.getText() + "'");

                JOptionPane.showMessageDialog(this, "Updated.", "Information", JOptionPane.INFORMATION_MESSAGE);

                this.dispose();
                Home h = new Home();
                h.setVisible(true);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

