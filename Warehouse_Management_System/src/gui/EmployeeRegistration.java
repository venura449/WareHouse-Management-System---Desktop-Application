    private void generateEmployeeID() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `employees` ORDER BY `employee_id` DESC LIMIT 1");
            if (resultSet.next()) {
                lastEmployeeID = resultSet.getString("employee_id");

            } else {
                lastEmployeeID = "EMP0000";
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

        // Increment the last employee ID
        int numericPart = Integer.parseInt(lastEmployeeID.substring(3));
        numericPart++;

        // Customize the employee ID format
        employeeID = String.format("EMP%04d", numericPart);

        jTextField4.setText(employeeID);
    }

    private void loadTypes() {

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `employee_type`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("type"));
                employeeTypeMap.put(resultSet.getString("type"), resultSet.getString("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadEmployees() {

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `employees`"
                    + "INNER JOIN `employee_type` ON `employees`.`employee_type_id`=`employee_type`.`id`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("employee_id"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("firstname"));
                vector.add(resultSet.getString("lastname"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("password"));
                vector.add(resultSet.getString("employee_type.type"));
                vector.add(resultSet.getString("registered_date"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

