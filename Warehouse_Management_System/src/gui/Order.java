    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String employeeId = jLabel18.getText();
        String sellerId = jTextField4.getText();
        String orderrId = jTextField1.getText();
        String stockId = jTextField3.getText();
        String qty = jFormattedTextField2.getText();

        if (sellerId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select seller.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (stockId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select stock.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (qty.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter quantity.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (Double.parseDouble(qty) > Double.parseDouble(jLabel21.getText())) {
            JOptionPane.showMessageDialog(this, "This quantity has not enough.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            Date date = new Date();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");

            try {
                MySQL.execute("INSERT INTO `order` (`idorder`,`stock_idstock`,`employees_employee_id`,`seller_idseller`,`qty`,`order_date`) VALUES "
                        + "('" + orderrId + "','" + stockId + "','" + employeeId + "','" + sellerId + "','" + qty + "','" + sdf.format(date) + "')");

                //update stock
                MySQL.execute("UPDATE `stock` SET `qty`=`qty`-'" + qty + "' WHERE `idstock`='" + stockId + "'");
                //update stock

                loadOrders("");
                reset();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

