    private String lastOrderID;
    String orderID;

    private void generateOrderID() {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `order` ORDER BY `idorder` DESC LIMIT 1");
            if (resultSet.next()) {
                lastOrderID = resultSet.getString("idorder");

            } else {
                lastOrderID = "ORD000000";
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

        // Increment the last employee ID
        int numericPart = Integer.parseInt(lastOrderID.substring(3));
        numericPart++;

        // Customize the employee ID format
        orderID = String.format("ORD%06d", numericPart);

        jTextField1.setText(orderID);
    }

    private void loadOrders(String id) {
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `order` INNER JOIN `seller` ON `order`.`seller_idseller`=`seller`.`idseller` WHERE `order`.`idorder` LIKE '" + id + "%'");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("idorder"));
                vector.add(resultSet.getString("stock_idstock"));
                vector.add(resultSet.getString("qty"));
                vector.add(resultSet.getString("seller.firstname"));
                vector.add(resultSet.getString("order_date"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

